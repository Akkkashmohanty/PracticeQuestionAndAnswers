# Write a Python program to print a list of space-separated elements.

# Sample list of elements
my_list = ["apple", "banana", "cherry", "date", "fig"]

# Join the elements with spaces and print the result
result = " ".join(my_list)

#In this program, we use the join method to concatenate the elements of the list with spaces between them. 
# The resulting string contains the elements separated by spaces, and we print that string.

print(result)
